##############################################################
##Playing with Numbers
##############################################################

##Basic arithmetic

#Addition

answer = 3 + 3

print(answer)

#Subtraction

answer_1 = 3 - 3

print(answer_1)

#Multiplication

answer_2 = 3 * 3

print(answer_2)

#Division

answer_3 = 3 / 3

print(answer_3)

#Exponents

answer_4 = 10 ** 2

print(answer_4)

#Order of Operations

answer_5 = 2 + 3 * 4

print(answer_5)

answer_6 = (2 + 3) * 4

print(answer_6)



##Floating numbers (aka decimals)

answer_7 = 0.1 + 0.1

print(answer_7)

answer_8 = 0.2 * 0.2

print(answer_8)

##Floats and Integers

answer_9 = 2 * 0.2

print(answer_9)

answer_10 = 4 + 0.7

print(answer_10)

answer_11 = 0.3 ** 2

print(answer_11)


##Underscores as numbers

answer_12 = 1_000_000_000

print(answer_12)


##Assigning multiple variables different values

answer_13, answer_14, answer_15 = 13, 17, 21

print("My answers are:\n\t",answer_13,"\n\t",answer_14,"\n\t",answer_15)


##Numbers as constants (use uppercase letters for naming variable)

ANSWER_16 = 999

print(ANSWER_16)
